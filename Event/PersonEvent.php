namespace Team\Event;

use Symfony\Component\EventDispatcher\Event;
use Team\Model\Person;

/**
 * Class PersonEvent
 * @package Team\Event
 */
class PersonEvent extends Event
{
    /**
     * @var Person $person
     */
    protected $person;

    /**
     * @return Person
     */
    public function getPerson()
    {
        return $this->person;
    }

    /**
     * @param Person $person
     */
    public function setPerson($person)
    {
        $this->person = $person;
    }

}