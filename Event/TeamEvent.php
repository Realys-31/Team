namespace Team\Event;

use Symfony\Component\EventDispatcher\Event;
use Team\Model\Team;

/**
 * Class TeamEvent
 * @package Team\Event
 */
class TeamEvent extends Event
{
    /** @var Team $team */
    protected $team;

    /**
     * @return Team
     */
    public function getTeam()
    {
        return $this->team;
    }

    /**
     * @param Team $team
     */
    public function setTeam($team)
    {
        $this->team = $team;
    }


}